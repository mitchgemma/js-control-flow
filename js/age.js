let age = 1;

if (age <=3){
    console.log("infant")
} else if (age<=7){
    console.log("toddler")
} else if (age<=12){
    console.log("preteen")
} else if (age<=19){
    console.log("teen")
} else {
    console.log("adult")
}



//"infant", "toddler", "child", "preteen", "teen", or "young adult", for an age that is an integer between 0 and 20.
